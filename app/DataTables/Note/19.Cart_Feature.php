/*
--------------------|153. 2_Cart Feature - Working with Product Popup Modal (Part - 2)|----------------

1. AJAX স্ক্যাফোল্ডিং তৈরি:
    - method, url, success, এবং error সহ AJAX এর বেসিক কাঠামো তৈরি করা হয়েছে।
    - console.log দিয়ে ডিবাগিং এবং যদি error আছে console.error এ ত্রুটি দেখানো হয়েছে।

2. Route তৈরি:
    web.php ফাইলে একটি নতুন গেট রুট (loadProductModel) তৈরি করা হয়েছে। সাতে product ID প্রদান করা হয়েছে।

3. global-script-blade.php এইটা ব্লেড ফাইলে রাউটের নাম ব্যবহার করতে URL: {{  }} ব্যবহার করা হয়েছে।
    -একটি placeholder যোগ করা হয়েছে :productId নামে এবং replace() ফাংশন ব্যবহার করে ডাইনামিক productId আইডি দিয়ে তা প্রতিস্থাপন করা হবে

4. Controller-এ নতুন মেথড:
    loadProductModel নামে একটি মেথড তৈরি করা হয়েছে, যা ডাটাবেজ থেকে প্রোডাক্ট ডেটা রিটার্ন করে।
    findOrFail দিয়ে প্রোডাক্ট খোঁজা হয়েছে, যা ভুল আইডি থাকলে ত্রুটি ফেলে।
    ইগার লোডিং: with() মেথড ব্যবহার করে প্রোডাক্টের সাথে সম্পর্কিত ডেটা (যেমন productSizes, productOptions) লোড করা হয়েছে।


 AJAX Request চেক: Network ট্যাবে AJAX রিকোয়েস্ট এবং রেসপন্স যাচাই করলাম রিলেশন ডেটাসহ প্রোডাক্ট ডেটা সঠিকভাবে এসেছে কিনা তা ডিবাগ করা হয়েছে।
*/



